    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
       if(this.txtNome.getText().trim().equals("")||
               this.TxtPreco.getText().trim().equals(" ")||
               this.TxtQuantidade.getText().trim().equals(" ")||
               this.txtCodigoBarras.getText().trim().equals(" ")){
            JOptionPane.showMessageDialog(rootPane, "Erro - todos os campos devem ser preenchidos");}
       else{
             Produto produto = new Produto();
             produto.setNome(this.txtNome.getText().trim());
             produto.setPreço(Double.parseDouble(this.TxtPreco.getText().trim()));
             produto.setQuantidade(Integer.parseInt(this.TxtQuantidade.getText().trim()));
             produto.setCodigobarra(this.txtCodigoBarras.getText().trim());
             
             ProdutoDAO dao = new ProdutoDAO("svc","root");
           try {
               dao.insere(produto);
               JOptionPane.showMessageDialog(null, "Produto inserido com sucesso");
               this.txtCodigoBarras.setText("");
               this.txtNome.setText("");
               this.TxtQuantidade.setText("");
               this.TxtPreco.setText("");
               this.TxtQuantidade.setText("");
               this.txtCodigo.setText(dao.GetNextId("produto", "codproduto")+"");
               lerDadosIniciais();
           } catch (SQLException ex) {
               Logger.getLogger(FrmProdutos.class.getName()).log(Level.SEVERE, null, ex);
           }
             
        
       }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarActionPerformed
         if (this.tabelaProdutos.getSelectedRow() == -1){
           JOptionPane.showMessageDialog(null,"Você deve selecionar o produto");
       }else{
           Produto produto = produtos.get(this.tabelaProdutos.getSelectedRow());
           int r = JOptionPane.showConfirmDialog(null, "Você deseja realmente apagar o produto "+produto.getNome());
           System.out.println(r);
           if(r == 0){
               ProdutoDAO dao = new ProdutoDAO ("svc","root");
               try {
                   dao.apagar(produto.getCodproduto());
                   JOptionPane.showMessageDialog(null, "Produto deletado com sucesso");
                   listarDadosPesquisados();
               } catch (SQLException ex) {
                   JOptionPane.showMessageDialog(null,"Erro :"+ex.getMessage());
               }
           }
    }//GEN-LAST:event_btnApagarActionPerformed
  }
