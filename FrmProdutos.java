    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if(this.controleDeAtualizacao == 0){
        if (this.tabelaProdutos.getSelectedRow() == -1){
           JOptionPane.showMessageDialog(null,"Você deve selecionar o produto");
       }else{
            Produto produto = produtos.get(this.tabelaProdutos.getSelectedRow());
            this.txtCodigo.setText(produto.getCodproduto()+"");
            this.txtNome.setText(produto.getNome());
            this.TxtPreco.setText(produto.getPreço()+"");
            this.TxtQuantidade.setText(produto.getQuantidade()+"");
            this.txtCodigoBarras.setText(produto.getCodigobarra());
            this.btnAtualizar.setText("Confirmar");
            this.controleDeAtualizacao = 1;
            this.btnInserir.setEnabled(false);
            this.btnApagar.setEnabled(false);
        }}else{
            ProdutoDAO dao = new ProdutoDAO ("svc","root");
            Produto produto = new Produto();
            produto.setCodproduto(Integer.parseInt(this.txtCodigo.getText().trim()));
            produto.setNome(this.txtNome.getText().trim());
            produto.setPreço(Double.parseDouble(this.TxtPreco.getText().trim()));
            produto.setQuantidade(Integer.parseInt(this.TxtQuantidade.getText().trim()));
            produto.setCodigobarra(this.txtCodigoBarras.getText().trim());           
            try {
                dao.atualiza(produto);
                this.controleDeAtualizacao = 0;
                this.btnAtualizar.setText("Atualizar");
                this.btnInserir.setEnabled(true);
                this.btnApagar.setEnabled(true);
                limparCampos();
                listarDadosPesquisados();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Erro :"+ex.getMessage());            
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

