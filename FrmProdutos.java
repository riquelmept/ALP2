    private void iniciaCodigo() {
        ProdutoDAO dao = new ProdutoDAO("svc","root");
        try {
            this.txtCodigo.setText( dao.GetNextId("produto", "codproduto")+"");
        } catch (SQLException ex) {
            Logger.getLogger(FrmProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }

    private void lerDadosIniciais() {
      ProdutoDAO dao = new ProdutoDAO("svc","root");
        try {
            produtos = dao.listarTodosProdutos();
            MyTableModel tab = new MyTableModel(Produto.class,produtos,tabelaProdutos);
            tabelaProdutos.setModel(tab);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Erro :"+ex.getMessage());
        }
    }
     private void listarDadosPesquisados() {
       ProdutoDAO dao = new ProdutoDAO("svc","root");
       String nome = this.txtPesquisa.getText().trim();
        try {
            produtos = dao.listarTodosProdutosPorNome(nome);
            MyTableModel tab = new MyTableModel(Produto.class,produtos,tabelaProdutos);
            tabelaProdutos.setModel(tab);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Erro :"+ex.getMessage());
        }
    
    }
     
     private void limparCampos() {
        ProdutoDAO dao = new ProdutoDAO("svc","root");
        this.txtNome.setText("");
        this.TxtPreco.setText("");
        this.TxtQuantidade.setText("");
        this.txtCodigoBarras.setText("");
        try {
            this.txtCodigo.setText(dao.GetNextId("produto", "codproduto")+"");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro :"+ex.getMessage());
        }
        
    }
    
    
    
