    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        double tem = Double.parseDouble(this.txtTem.getText().trim().replace(",","."));
        double deveria = Double.parseDouble(this.TxtDeveria.getText().trim().replace(",", "."));
        double saldo = tem- deveria;
        caixa.setDatafechamento(new Date());
        caixa.setValorfinal(tem);
        caixa.setDiferença(saldo);
        caixa.setAberto(0);
        CaixaDAO dao =  new CaixaDAO("svc","root");
        try {
            dao.fechaCaixa(caixa);
            JOptionPane.showMessageDialog(null, "Caixa Fechado com sucesso!");
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(FrmFecharCaixa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

