    private void lerDadosIniciais() {
        CaixaDAO dao = new CaixaDAO("svc","root");
        try {
            caixa = dao.lerCaixa();
            double valor = caixa.getValorinicial()+caixa.getVendas()+caixa.getSuprimentos()-
                    caixa.getDeposito();
            this.TxtDeveria.setText(valor+"");
                    
        } catch (SQLException ex) {
            Logger.getLogger(FrmFecharCaixa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
