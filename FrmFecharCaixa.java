    private void txtTemFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTemFocusLost
        double tem = Double.parseDouble(this.txtTem.getText().trim().replace(",","."));
        double deveria = Double.parseDouble(this.TxtDeveria.getText().trim().replace(",", "."));
        double saldo = tem- deveria;
        if(saldo > 0.0){
            this.TxtSituacao.setText("Passou R$"+saldo);  
        } else if(saldo<-0.00000000){
            this.TxtSituacao.setText("Faltou R$"+saldo);
        }else{
            this.TxtSituacao.setText("Caixa Correto!");
        }
    }//GEN-LAST:event_txtTemFocusLost

